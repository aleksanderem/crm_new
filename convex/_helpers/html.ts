/**
 * HTML escaping utility for safe interpolation of user-supplied values
 * into HTML templates. Use before inserting any untrusted string into HTML.
 */
export function escapeHtml(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}
